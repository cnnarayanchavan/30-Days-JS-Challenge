arr = [];

//push
arr.push(1,2,3,4,5,6);
console.log(arr);  //append element from last



//pop
arr.pop();
console.log(arr);  //remove element from last



//shift 
arr.shift();
console.log(arr);  //remove element from start of an array //2,3,4,5



//unshift
console.log(arr.unshift());         //no use...it will just return the length of an array 
array = [15,6,7,8];
array.unshift(12,23,34);
console.log(array);            //it will add element into an array from start of an array